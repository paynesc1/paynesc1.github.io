document.addEventListener("DOMContentLoaded", function(){
    document.getElementById("button").addEventListener("click", async function getCoderData(){
        var response = await fetch("https://api.github.com/users/paynesc1")
        var coderData = await response.json();
        console.log(coderData);

        var avatar = document.getElementById('avatar')
        avatar.src = coderData.avatar_url;
        avatar.style.display = "block";

        var html_url = document.getElementById('html_url')
        html_url.href = coderData.html_url;
        html_url.style.display = "block";

    })
    getCoderData();
});